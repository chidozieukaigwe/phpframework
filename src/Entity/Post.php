<?php
namespace App\Entity;

use ChidoUkaigwe\Framework\Dbal\Entity;

class Post extends Entity
{
    public function __construct(
    private ?int $id, 
    private string $title,
    private string $body,
    private \DateTimeImmutable $createdAt
    ){}

    public static function create(string $title, string $body, ?int $id = null, ?\DateTimeImmutable $createdAt = null): self
    {
        return new self($id, $title, $body, $createdAt?? new \DateTimeImmutable());
    }

    /**
     * Get the value of title
     */ 
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * Set the value of title
     *
     * @return  self
     */ 
    public function setTitle($title)
    {
        $this->title = $title;

        return $this;
    }


    /**
     * Get the value of body
     */ 
    public function getBody()
    {
        return $this->body;
    }

    /**
     * Set the value of body
     *
     * @return  self
     */ 
    public function setBody($body)
    {
        $this->body = $body;

        return $this;
    }

    /**
     * Get the value of id
     */ 
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set the value of id
     *
     * @return  self
     */ 
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * Get the value of createdAt
     */ 
    public function getCreatedAt()
    {
        return $this->createdAt;
    }

    /**
     * Set the value of createdAt
     *
     * @return  self
     */ 
    public function setCreatedAt($createdAt)
    {
        $this->createdAt = $createdAt;

        return $this;
    }
}